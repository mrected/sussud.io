document.addEventListener('keydown', getKey)

const sections = Array.from(document.querySelectorAll('.led-col'))



function getKey(e) {
    sections.forEach(section => section.classList.remove('led-on')
        )
    const key = e.key.toLowerCase()
    if(key in patterns){
        patterns[key].forEach(section => {
            // console.log(document.querySelector(`.${section}`))
            // document.querySelector(`.${section}`).style.backgroundColor = '#bada55'
            document.querySelector(`.${section}`).classList.add('led-on')
        });
    }
}

